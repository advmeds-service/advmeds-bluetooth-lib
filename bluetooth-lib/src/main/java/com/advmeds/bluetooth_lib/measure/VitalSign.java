package com.advmeds.bluetooth_lib.measure;

public class VitalSign {
    /// 收縮壓
    String systolic = "";
    /// 舒張壓
    String diastolic = "";
    /// 脈搏
    String pulse = "";
    /// 血糖
    String glucose = "";
    /// 血氧
    String spo2 = "";
    /// 體溫
    String temperature = "";
    /// 尿酸
    String uricAcid = "";
    /// 血酮
    String ketone = "";
    /// 膽固醇
    String cholesterol = "";
    /// 血紅素(HB)
    String hemoglobin = "";
    /// 紅血球容積比(HC)
    String hematocrit = "";
    /// 血中乳酸
    String lactate = "";
    /// 體重
    String weight = "";

    public String getSystolic() {
        return systolic;
    }

    public void setSystolic(String systolic) {
        this.systolic = systolic;
    }

    public String getDiastolic() {
        return diastolic;
    }

    public void setDiastolic(String diastolic) {
        this.diastolic = diastolic;
    }

    public String getPulse() {
        return pulse;
    }

    public void setPulse(String pulse) {
        this.pulse = pulse;
    }

    public String getGlucose() {
        return glucose;
    }

    public void setGlucose(String glucose) {
        this.glucose = glucose;
    }

    public String getSpo2() {
        return spo2;
    }

    public void setSpo2(String spo2) {
        this.spo2 = spo2;
    }

    public String getTemperature() {
        return temperature;
    }

    public void setTemperature(String temperature) {
        this.temperature = temperature;
    }

    public String getUricAcid() {
        return uricAcid;
    }

    public void setUricAcid(String uricAcid) {
        this.uricAcid = uricAcid;
    }

    public String getKetone() {
        return ketone;
    }

    public void setKetone(String ketone) {
        this.ketone = ketone;
    }

    public String getCholesterol() {
        return cholesterol;
    }

    public void setCholesterol(String cholesterol) {
        this.cholesterol = cholesterol;
    }

    public String getHemoglobin() {
        return hemoglobin;
    }

    public void setHemoglobin(String hemoglobin) {
        this.hemoglobin = hemoglobin;
    }

    public String getHematocrit() {
        return hematocrit;
    }

    public void setHematocrit(String hematocrit) {
        this.hematocrit = hematocrit;
    }

    public String getLactate() {
        return lactate;
    }

    public void setLactate(String lactate) {
        this.lactate = lactate;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
}
